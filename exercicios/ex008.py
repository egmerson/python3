#Conversão de medidas
n1 = float(input('Digite um valor em metros: '))
print('O valor em km é: {}'.format(n1/1000))
print('O valor em hm é: {}'.format(n1/100))
print('O valor em dm é: {}'.format(n1/10))
print('O valor em dm é: {}'.format(n1*10))
print('O valor em cm é: {}'.format(n1*100))
print('O valor em mm é: {}'.format(n1*1000))